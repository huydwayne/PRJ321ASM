package sample.products;

/**
 *
 * @author HiGien
 */
public class ProductErrorMessage {
    private String errorId;
    private String errorName;
    private String errorPrice;
    private String errorDescription;
    private String errorDuplicate;
    private String errorImage;

    public String getErrorImage() {
        return errorImage;
    }

    public void setErrorImage(String errorImage) {
        this.errorImage = errorImage;
    }
     
    
    
    public String getErrorId() {
        return errorId;
    }

    public void setErrorId(String errorId) {
        this.errorId = errorId;
    }

    public String getErrorName() {
        return errorName;
    }

    public void setErrorName(String errorName) {
        this.errorName = errorName;
    }

    public String getErrorPrice() {
        return errorPrice;
    }

    public void setErrorPrice(String errorPrice) {
        this.errorPrice = errorPrice;
    }

    public String getErrorDescription() {
        return errorDescription;
    }

    public void setErrorDescription(String errorDescription) {
        this.errorDescription = errorDescription;
    }

    public String getErrorDuplicate() {
        return errorDuplicate;
    }

    public void setErrorDuplicate(String errorDuplicate) {
        this.errorDuplicate = errorDuplicate;
    }
    
    
}
